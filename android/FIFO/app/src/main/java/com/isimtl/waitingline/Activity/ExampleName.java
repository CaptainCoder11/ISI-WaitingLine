package com.isimtl.waitingline.Activity;

public class ExampleName {
    private int mImage;
    private String mText1;
    private String mText2;
    private String mText3;

    public ExampleName (int imageResource, String text1, String text2, String text3){
        mImage = imageResource;
        mText1 = text1;
        mText2 = text2;
        mText3 = text3;
    }

    public int getmImage() {
        return mImage;
    }



    public String getmText1() {
        return mText1;
    }



    public String getmText2() {
        return mText2;
    }



    public String getmText3() {
        return mText3;
    }


}
